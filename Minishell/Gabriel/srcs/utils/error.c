#include "../minishell.h"

/**
 * @brief Print error message
 * 
 * @param cmd Command that caused error
 * @param arg Argument that caused error
 * @param msg Error message
 */
void	print_error(char *cmd, char *arg, char *msg)
{
	ft_putstr_fd("minishell: ", 2);
	if (cmd)
	{
		ft_putstr_fd(cmd, 2);
		ft_putstr_fd(": ", 2);
	}
	if (arg)
	{
		ft_putstr_fd(arg, 2);
		ft_putstr_fd(": ", 2);
	}
	if (msg)
		ft_putstr_fd(msg, 2);
	ft_putstr_fd("\n", 2);
}

/**
 * @brief Print syntax error
 * 
 * @param token Token that caused error
 */
void	syntax_error(char *token)
{
	ft_putstr_fd("minishell: syntax error near unexpected token `", 2);
	ft_putstr_fd(token, 2);
	ft_putstr_fd("'\n", 2);
}

/**
 * @brief Print error message and return value
 * 
 * @param msg Error message
 * @param ret Return value
 * @return int Return value
 */
int	error_return(char *msg, int ret)
{
	if (msg)
		print_error(NULL, NULL, msg);
	return (ret);
}
